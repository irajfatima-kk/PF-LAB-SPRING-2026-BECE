#include <stdio.h>

int main() {
    int age = 19;
    float cgpa = 3.8;
    double exact_value = 123.456789;
    char section = 'A';

    printf("Integer: %d\n", age);
    printf("Float: %f\n", cgpa);
    printf("Double: %lf\n", exact_value);
    printf("Character: %c\n", section);

    return 0;
}
